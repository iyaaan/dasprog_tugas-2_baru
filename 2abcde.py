# Membuat list berisi 5 nama buah
fruit = ["Apel", "Pisang", "Mangga", "Jeruk", "Semamngka"]

# Menampilkan seluruh isi list
print("Daftar buah:", fruit)

# Menampilkan buah pada indeks ke-2 (index dimulai daro 0)
print("Buah pada index ke-2:", fruit[2])

# Menambahkan satu nama buah baru ke dalam list
fruit.append("nanas")

# Menampilkan list setelah penambahan
print("Daftar buah setelah ditambah:", fruit)